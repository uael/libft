#include "libft.h"

inline void	*ft_memchr(void const *ptr, t_i32 c, t_usz n)
{
	while (n--)
		if (*(t_u08*)ptr++ == (t_u08)c)
			return ((void *)ptr - 1);
	return (NULL);
}
