#include "libft.h"

inline t_i32	ft_strncmp(t_cstr s1, t_cstr s2, t_usz n)
{
	while (n-- && *s1 && (*s1 == *s2))
	{
		++s1;
		++s2;
	}
	return (n == 0 ? 0 : (t_u08)*s1 - (t_u08)*s2);
}
