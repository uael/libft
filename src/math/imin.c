#include "libft/math.h"

inline int8_t	ft_i8min(int8_t a, int8_t b)
{
	return (MIN(a, b));
}

inline int16_t	ft_i16min(int16_t a, int16_t b)
{
	return (MIN(a, b));
}

inline int32_t	ft_i32min(int32_t a, int32_t b)
{
	return (MIN(a, b));
}

inline int64_t	ft_i64min(int64_t a, int64_t b)
{
	return (MIN(a, b));
}

inline ssize_t	ft_imin(ssize_t a, ssize_t b)
{
	return (MIN(a, b));
}
